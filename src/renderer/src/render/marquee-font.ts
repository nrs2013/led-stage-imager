/** Place a point every `step` of arc length along each subpath (start included). */
export function sampleStrokes(strokes: Pt[][], step: number): Pt[] {
  const out: Pt[] = []
  for (const poly of strokes) {
    if (!poly.length) continue
    out.push(poly[0])
    let carry = 0
    for (let i = 1; i < poly.length; i++) {
      const [ax, ay] = poly[i - 1]
      const [bx, by] = poly[i]
      const segLen = Math.hypot(bx - ax, by - ay)
      if (segLen === 0) continue
      let d = step - carry
      while (d <= segLen + 1e-9) {
        const t = d / segLen
        out.push([ax + (bx - ax) * t, ay + (by - ay) * t])
        d += step
      }
      carry = (carry + segLen) % step
    }
  }
  return out
}

/** Drop points closer than minDist to an already-kept one (merges junction overlaps). */
export function dedupe(pts: Pt[], minDist: number): Pt[] {
  const kept: Pt[] = []
  const m2 = minDist * minDist
  for (const p of pts) {
    let ok = true
    for (const k of kept) {
      const dx = k[0] - p[0]
      const dy = k[1] - p[1]
      if (dx * dx + dy * dy < m2) {
        ok = false
        break
      }
    }
    if (ok) kept.push(p)
  }
  return kept
}
