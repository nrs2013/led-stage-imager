export type Pt = [number, number]
export interface Glyph {
  w: number
  strokes: Pt[][]
}

/** Arc as a polyline. Screen coords (y down): 0°=right, 90°=down, 180°=left, 270°=up. */
function arc(cx: number, cy: number, r: number, d0: number, d1: number, n = 12): Pt[] {
  const out: Pt[] = []
  const a0 = (d0 * Math.PI) / 180
  const a1 = (d1 * Math.PI) / 180
  for (let i = 0; i <= n; i++) {
    const a = a0 + ((a1 - a0) * i) / n
    out.push([cx + Math.cos(a) * r, cy + Math.sin(a) * r])
  }
  return out
}
